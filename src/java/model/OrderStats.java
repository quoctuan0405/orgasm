package model;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class OrderStats {
    private Date date;
    private double total;

    public OrderStats() {
        // Comment
    }

    public OrderStats(Date date, double total) {
        this.date = date;
        this.total = total;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
    
}